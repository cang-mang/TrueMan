pub fn x32(key: &[u8]) -> u32 {
    let mut h = 2166136261_u32;
    for k in key {
        h = h.wrapping_mul(16777619_u32) ^ *k as u32;
    }

    h
}
/*----------------------------------------------------------------------------*/
pub fn x64(key: &[u8]) -> u64 {
    let mut h = 14695981039346656037_u64;
    for k in key {
        h = h.wrapping_mul(1099511628211_u64) ^ *k as u64;
    }

    h
}
